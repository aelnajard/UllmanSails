  <h1>Victory in the Quarter Ton Cup</h1>
  <br>
  <img src="../../News/Content/3.VictoryintheQuarterTonCup/1.quarterton1107x594.jpeg" alt="">
  <br><br>
  <p>Ullman Sails customer John Santy recently cruised to victory at the Quarter
    Ton Cup. Read his amusing account of how he and the team got there:</p>
    <br>
  <p>After the 2018 Quarter Ton cup came and went, and after 9 years of trying
    to win this crazy little event, words were uttered on the dockside. “We are
    done with this little ship, if we are to keep doing this thing, we need a
    thoroughbred with history to take on the Cowes Mafia.”</p>
    <br>
  <p>Due diligence was done on a range of steads. As fate would have it, our
    stead of choice happened to be in Hamble, not Timbuktu! Lacydon Protis, the
    1981 Marseille Quarter Ton cup winner was in town. Visits were made to view,
    discussions had, and offers suggested.</p>
    <br>
  <p>We were reminded that Quarter Ton sailing was all about bringing old friends
     together and having fun. But fun is winning when it comes to this group of
     diehards. “We can’t have our heads kicked in anymore,” and “We need to bring
     a knife to the knife fight,” were the phrases that rang true and loud.</p>
     <br>
  <p>After one big boozy Thursday steak night, “OK, let’s do it!” went the roar.</p>
  <br>
  <img src="../../News/Content/3.VictoryintheQuarterTonCup/2.quartertoncup.jpg" alt="">
  <br><br>
  <h1>New Hope for the Quarter Ton Cup</h1>
  <br><br>
  <p>The deed was done on the 4th September, and Lacydon Protis had a new stable.
    Then, like a speeding white Ford Sierra on its way back from Twin Town,
    we took the reins of Protis, as she will now be called. We went sailing for
    the first time on the 6th of October 2018, after a huge bit of rig jiggery-pokery,
    kit swapping and cradle making. A few racing days with old ill-fitting headsails
    and chutes at the Hamble Winter Series 2018 resulted in 3 bullets and a lot of grins.
    The last racing being the 9th of December in 30kts with no falling over.</p>
    <br>
    <p>Is it, will it be, could it be the one, our minds dared to dream as winter was upon us.</p>
    <br>
    <p>A master plan was hatched, and the big year ahead starts, with design
      debates, sail debates, rating debates, boat-to-go-undercover debates,
      remove-pink debates etc. The list got ever bigger, but how do we stop the
      deadly leading duo? “You can’t,” was the reply, “It’s too late!”</p>
      <br>
    <p>And so, came the winter boat work phase. The removal of anything and
      everything was the order of the day, and the midnight oil was burnt. Bodies,
      minds, and relationships were pushed to the limit in honorable pursuit of
      this our most Holy of sailing grails.</p>
      <br>
    <p>A change of venue arrived and a trip to the Dark Side found us sheltered
      with new friends willing to do honest toil on the east shore. February and
      March past in a flash.</p>
      <br>
    <p>High altitude training had commenced for some members of the team with
      limited success. The major job list almost complete, we ventured back home
      to Hamble and got ready to launch. This ended up being delayed due to rudder
      issues and real paying work.</p>
    <br>
    <h1>The Quarter Ton Sailing Story for 2019</h1>
    <br>
    <p>The start of the 2019 Warsash spring series arrived and went. We finally
      splashed the boat for the first time on Saturday the 27th of April with
      some dock test fitting of the newly arrived Ullman fruit. Yes, we had new
      sails earlier than the first race of the Quarter Ton Cup, what could possibly go wrong!</p>
      <br>
    <p>A massive thank you to Jon Pegg and Bruce Hollis from Ullman Sails for
      designing and manufacturing some great sails! We finally went sailboat
      racing for the first time in the new ship on April the 28th, for race 8
      and 9 of the series. Our old foes were waiting for us and had been practising
      hard for the battles ahead. We locked horns for the first two races of our
      season and started as we hoped we would go on, with a score of a 3rd and a
      1st. The games have commenced.</p>
      <br>
    <p>Then quickly arrived the Vice Admirals Cup which resulted in a 2nd overall
       by 1.5 pts with a score of 1, 2, 4, 3, 2, 1.5, 2, 2.</p>
       <br>
    <p>Is it, will it be, could it be the one? Our minds dared to dream as the
       Quarter Ton cup was almost upon us.</p>
    <br>
    <img src="../../News/Content/3.VictoryintheQuarterTonCup/3.quartertonboatullmansails.jpeg" alt="">
    <br><br>
    <h1>The Quarter Ton Cup Dawns</h1>
    <br>
    <p>A few sailing days and sail testing continued. One race also got fitted
      in during the first Royal Southern May Regatta which again resulted in a
      light air bullet. Much rum and Marty dancing was done.</p>
      <br>
    <p>Some team members then went off to sail in other events and take a much
      needed holiday, in readiness for the BIG one to come.</p>
      <br>
    <p>Then the Holy grail event had arrived, with a very high-quality top 10
      turn out and all the usual faces. The nerves had started. The rest, as they
      say, is now in folk law and written large in history for evermore.</p>
    <br>
    <img src="../../News/Content/3.VictoryintheQuarterTonCup/4.quartertonullmansails.jpg" alt="">
    <br><br>
    <p>Team Hamble, after 10 years, had finally delivered that which was long
      overdue. Protis wins the <a href="https://www.quartertoncup.org">2019 Quarter
        Ton Cup 1,</a> (5), 2, 1, 1, 1.5, 3, 2, 2.
      With a score of 13.5 pts against second place on 31 pts and third on 41.5.
      The biggest winning margin since the Quarter Ton Cup was relaunched!</p>
      <br>
    <p>It was pure joy to sit and watch our great leader head off across the
      horizon to the unbeatable position of a Quarter Ton cup win with a race to spare!</p>
    <br>
    <img src="../../News/Content/3.VictoryintheQuarterTonCup/5.quartertonboat.jpeg" alt="">
    <br><br>
    <h1>What’s Next For The Boat?</h1>
    <br>
    <p>“What next?” I hear you shout.</p>
    <br>
    <p>The sailing year is not yet over! Other friends arrived to assist us with
      the continued lower-key events of the Protis season. July 5th to 7th, the
      IRC Nationals arrived with all manner of vessels large and small from 26′
      to 47′ coming to take on the mighty Protis. Once we had understood that we
      were not level racing and we were the smallest boat in our fleet, we came
      up with a plan and much fun was had with a 2nd overall in class 4 with a
      score of 5, 3, 4, 3, 3, 4, 1, 1, losing out to a very well sailed J109
      Jubilee in weather that suited the bigger boats on the first few days.</p>
    <br>
    <p>The lower-key summer of no-discontent then continued. The Tattinger Regatta
       in Yarmouth was booked in for 27th to 28th July 2019, a three-race schedule
       in Class 4 over two days. Much high jinx adventure was had and there is no
       point in boring you with the details of ribs, fuel, flat batteries, deliveries,
       more batteries, ferry trips, and ever dissolving crew numbers for all manner
       of reasons. A new sport of three-man Quarter Ton sailing was born, which
       helped to create the mythical story of team Protis sailing faster than the
       race committee could have anticipated resulting in a DNF, commuted to a win
       by over 30mins. This all played out to give us a score of 3 x 1sts in a fleet
       of 27 and an overall regatta win for Protis against 213 other boats!</p>
    <br>
    <p>The Hamble Winter Series has now arrived, which brings us full circle to
      where the Protis sailing year had started back in October of 2018. Team
      Protis has so far delivered 8 firsts with hopefully more to come with the
      series finishing on the 1st of December.</p>
    <br>
    <p>So just to recap the year in race numbers – Protis has competed in 40
      races, of which we won 23 races, and we failed to make the podium on only
      5 outings, with our worst result being a 5th an unbelievable record.</p>
    <br>
    <p>So to answer the recurring question of the earlier season – Is it,
      will it be, could it be the one?
    Yes, it is, yes, it will be, YES, it’s the one!</p>
    <br>
