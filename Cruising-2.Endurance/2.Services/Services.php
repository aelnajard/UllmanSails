  <h1>Endurance Series</h1>
  <p>Endurance sails are our most durable product, specifically designed for offshore and passage-making cruisers. Each sail is constructed and reinforced to withstand the rigorous demands of long-distance sailing and prolonged exposure to the elements. The Endurance series is the strong, dependable option for the bluewater cruising sailor.</p>
  <h2>Strength in the Details</h2>
  <p>The standard finishing of Endurance sails are an upgraded version of Ullman Sails Navigator cruising sail. These construction upgrades make the sail stronger, which increases durability and adds more miles and years to your sailing enjoyment.</p>
  <ul>
    <li>Multiple Rows of Triple-Step Stitching on Every Seam</li>
    <li>Over-Sized Patching at All Corners and Reefs</li>
    <li>U.V. Resistant Thread for All Stitching (seam coating is available)</li>
    <li>Double Leech Tapes</li>
    <li>Additional Reinforcements at Each Slide</li>
    <li>Flutter Patches at Leech of Every Seam</li>
    <li>Heavier, Stronger Slides at Battens and Reef Points</li>
    <li>Stainless Steel Rings with Webbing Load Straps</li>
    <li>Sunbrella U.V. Covers on Roller Furling Sails</li>
  </ul>
  <h2>Design and Construction</h2>
  <p>Every Endurance sail is custom designed by the Ullman Sails Design Team to make your boat easier to sail and handle as conditions change. Mainsails have additional reinforcement in high-load areas so you can keep your sails reefed over extended periods of time without damaging the sail.</p>
  <p>Genoas are also designed to be roller reefed with extra reinforcement at the head and tack to prolong sail longevity and prevent sail distortion.</p>
  <h2>Sail Cloth Selection and Performance</h2>
  <p>Endurance Series sails are available in a variety of constructions. Ullman Sails will work with you to select the most appropriate material and construction to match your cruising goals and budget.</p>

  <ul>
    <li><strong>Woven Dacrons:</strong>  High-quality, high-modulus Dacron is standard in the Endurance Series. Dacron withstands exposure to the elements and high-wind flogging better than any other sail material, providing the greatest longevity of any sail. Available in cross-cut or radial constructions.</li>
    <li><strong>Taffeta-coated cruise laminates:</strong> With added taffeta for durability and UV protection, these laminate sails are ideal for performance-minded cruisers. Cruise laminates have low stretch and superior load management allowing the sails to hold their intended shape even longer when compared to Dacron. Available in cross-cut and radial constructions.</li>
    <li><strong>FiberSport Cruise:</strong>  Utilising the same technology as our FiberPath Enduro product from the Voyager Series, these sails combine polyester fibre with taffeta for added durability. Polyester, rather than aramid, carbon, or vectran fibres, offers cruisers some performance advantages of a FiberPath custom membrane at a more competitive price.</li>
  </ul>
  <h2>Gore® Tenara® Thread</h2>
  <p>Endurance and Voyager Series sails can be upgraded to use Gore® Tenara® thread throughout the entire sail. Gore® Tenara® thread is hydrophobic and completely unaffected by UV degradation, salt water, extreme weather, chemicals, acid rain and more. Considering the importance of stitching, Ullman Sails strongly endorses using Gore® to hold your sail together.</p>


